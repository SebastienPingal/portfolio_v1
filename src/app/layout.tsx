import type { Metadata } from "next"
import { cereal } from './font'
import "./globals.css"

import SessionProvider from "./SessionProvider"
import { ThemeProvider } from "./theme-provider"

import Navbar from "@/components/NavBar"
import PerlinBackground from "@/components/perlinBackground.jsx"
import { Toaster } from "@/components/ui/toaster"

import { getPosts } from "./actions"

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const workPosts = await getPosts("WORK")
  const blogPosts = await getPosts("BLOGPOST")
  return (
    <html lang="en" suppressHydrationWarning>
      <body className={[cereal.className, "flex w-screen h-screen overflow-hidden"].join(" ")}>
        <Toaster />
        <ThemeProvider
          attribute="class"
          defaultTheme="dark"
          enableSystem
        >
          <SessionProvider>
            <Navbar workPosts={workPosts} blogPosts={blogPosts} />
            <main className="flex-1 page z-10 max-w-4xl mx-auto p-8 overflow-auto">{children}</main>
            <PerlinBackground />
          </SessionProvider>
        </ThemeProvider>
      </body>
    </html>
  )
}
